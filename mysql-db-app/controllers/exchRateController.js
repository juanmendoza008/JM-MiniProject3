"use strict";
import { ExchRate } from "../models/index.js";

// finds all users in DB, then sends array as response //Get
const getExchRates = (res) => {
  ExchRate.findAll({})
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};
// uses JSON from request body to create new user in DB //Post
const createExchRate = (data, res) => {
  ExchRate.create(data)
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};

//Put update user
const updateExchRate = (req, res) => {
  ExchRate.update(req.body, {
    where: { id: req.params.id },
    returning: true,
  })
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};
// Delete user matching ID from params
const deleteExchRate = (req, res) => {
  ExchRate.destroy({ where: { id: req.params.id } })
    .then((data) => {
      res.send({ result: 200, data: data });
    })
    .catch((err) => {
      console.log(err);
      res.send({ result: 500, error: err.message });
    });
};
export default { getExchRates, createExchRate, updateExchRate, deleteExchRate };
